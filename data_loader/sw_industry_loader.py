# 股票代码 → 行业映射缓存（全局变量，避免重复查询）
_STOCK_TO_INDUSTRY_CACHE: Dict[str, str] = {}


def get_sw1_industry_codes() -> Dict[str, str]:
    """
    获取申万一级行业代码映射表

    SQLite 模式：直接使用内置映射表。
    """
    return SW1_INDUSTRY_CODES.copy()


def get_stock_industry(stock_code: str) -> Optional[str]:
    """
    根据股票代码查询所属申万一级行业

    Args:
        stock_code: 股票代码（如 "000001", "600000"）

    Returns:
        行业名称（如 "银行"），未找到返回 None
    """
    global _STOCK_TO_INDUSTRY_CACHE

    # 检查缓存
    if stock_code in _STOCK_TO_INDUSTRY_CACHE:
        return _STOCK_TO_INDUSTRY_CACHE[stock_code]

    try:
        # 使用内置映射表（SQLite 模式下无法调用 AkShare API）
        industry_codes = get_sw1_industry_codes()

        # 内置映射表是二级子行业，无法精确查询成分股
        # 使用关键词匹配的备用方案
        logger.debug(f"[get_stock_industry] {stock_code} 使用关键词匹配")
        return None

    except Exception as e:
        logger.warning(f"[get_stock_industry] 查询失败: {e}")
        return None


